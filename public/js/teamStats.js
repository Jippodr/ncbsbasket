function showBasic() {
    var basic = document.getElementById("teamBasic");
    var shooting = document.getElementById("teamShooting");
    var advanced = document.getElementById("teamAdvanced");
    var miscellaneous = document.getElementById("teamMiscellaneous");
    var basicTotal = document.getElementById("teamBasicTotal");
    var basic36 = document.getElementById("teamBasic36");
    var advanced36 = document.getElementById("teamAdvanced36");
    var miscellaneousTotal = document.getElementById("teamMiscellaneousTotal");

    basic.style.display = "block";
    shooting.style.display = "none";
    advanced.style.display = "none";
    miscellaneous.style.display = "none";
    basicTotal.style.display = "none";
    basic36.style.display = "none";
    advanced36.style.display = "none";
    miscellaneousTotal.style.display = "none";
     
}

